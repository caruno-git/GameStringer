files.forEach(file => {
  const lang = file.replace('.json', '');
  const filePath = path.join(localesDir, file);
  const data = JSON.parse(fs.readFileSync(filePath, 'utf8'));
  
  if (!data.detailedStatistics) data.detailedStatistics = {};
  Object.assign(data.detailedStatistics, translations[lang] || translations.en);
  
  fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n', 'utf8');
  console.log(`✓ ${file}: aggiunte chiavi detailedStatistics`);
});

